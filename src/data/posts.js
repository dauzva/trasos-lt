// Helper function to get posts by category
export const getPostsByCategory = (mainCategory, subCategory, subSubCategory = null) => {
  const categoryKey = mainCategory?.toLowerCase()
  const subCategoryKey = subCategory?.toLowerCase()
  const subSubCategoryKey = subSubCategory?.toLowerCase()
  
  if (subSubCategoryKey && samplePosts[categoryKey]?.[subSubCategoryKey]) {
    return samplePosts[categoryKey][subSubCategoryKey]
  }
  
  if (samplePosts[categoryKey]?.[subCategoryKey]) {
    return samplePosts[categoryKey][subCategoryKey]
  }
  
  return []
}

// Helper function to get a single post by ID
export const getPostById = (postId) => {
  for (const mainCategory of Object.values(samplePosts)) {
    for (const subCategory of Object.values(mainCategory)) {
      const post = subCategory.find(p => p.id === parseInt(postId))
      if (post) return post
    }
  }
  return null
}

// Helper function to get all posts
export const getAllPosts = () => {
  const allPosts = []
  for (const mainCategory of Object.values(samplePosts)) {
    for (const subCategory of Object.values(mainCategory)) {
      allPosts.push(...subCategory)
    }
  }
  return allPosts.sort((a, b) => new Date(b.date) - new Date(a.date))
}

